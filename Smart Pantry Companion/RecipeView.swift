import UIKit

class RecipeView: UITableViewController {
    
    var item = RecipeItem()
    
    override func viewDidLoad() {
        super.viewDidLoad()

        if let defaults = UserDefaults.standard.data(forKey: "key"),
            let i = try? JSONDecoder().decode(RecipeItem.self, from: defaults) {
                item = i
                self.navigationItem.title = item.name
                
        }
    }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return item.ingredients.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "recipe", for: indexPath)

        // Configure the cell...
        cell.textLabel?.text = item.ingredients[indexPath.row][0]
        cell.detailTextLabel?.text = item.ingredients[indexPath.row][1]
        return cell
    }

